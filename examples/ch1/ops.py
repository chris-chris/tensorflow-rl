# -*- encoding : utf-8 -*-

x = 7
print("x = 7")
print("x :", x)

y = 3
print("y = 3")
print("y :", y)

# 더하기(add)
x = 7
y = 3
z = x + y
print("x + y :", z)
# x + y : 3

# 빼기(subtract)
x = 7
y = 3
z = x - y
print("x - y :", z)
# x - y : -1

# 곱하기(multiply)
x = 7
y = 3
z = x * y
print("x * y :", z)
# x * y : 20

# 지수(exponential)
x = 7
y = 3
z = x ** y
print("x ** y :", z)
# x ** y : 1024

# 나눗셈(division)
x = 7
y = 3
z = x / y
print("x / y :", z)
# x / y : 2.3333333333333335

# 나머지(remainder)
x = 7
y = 3
z = x % y
print("x % y :", z)
# x % y : 2

# // 연산
x = 7
y = 3
z = x // y
print("x // y :", z)
# x // y : 2

# += 연산자
x = 7
x += 10
print("x = 7")
print("x += 10")
print("x:", x)
# x: 11

# *= 연산자
x = 7
x *= 3
print("x = 7")
print("x *= 3")
print("x :", x)
# x : 6

# /= 연산자
x = 7
x /= 3
print("x = 7")
print("x /= 3")
print("x :", x)
# x : 2.3333333333333335

# type 함수(type function)
x = 7
print("x = 7")
print("type(x) :", type(x))
# type(x) : <class 'int'>

y = 1.5
print("y = 1.5")
print("type(y) :", type(y))
# type(y) : <class 'float'>